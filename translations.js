window.setLanguage = function(lang) {
    console.log('Setting language to:', lang);
    const translation = translations[lang];
    if (!translation) {
        console.error('Translation not found for language:', lang);
        return;
    }

    try {
        localStorage.setItem('selectedLanguage', lang);
    } catch (e) {
        console.warn('LocalStorage not available');
    }
    
    document.documentElement.lang = lang;
    document.documentElement.dir = translation.dir;
    document.body.style.fontFamily = translation.font;

    // Add Arabic Google Font if not present
    if (lang === 'ar' && !document.getElementById('arabic-font')) {
        const link = document.createElement('link');
        link.id = 'arabic-font';
        link.rel = 'stylesheet';
        link.href = 'https://fonts.googleapis.com/css2?family=Amiri:wght@400;700&display=swap';
        document.head.appendChild(link);
    }

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translation[key]) {
            el.innerHTML = translation[key];
            // If it's a link with no content, it might need the key name if specifically requested
            // but usually we want the translation string which might contain HTML
        } else {
            console.warn('Missing translation key:', key, 'for language:', lang);
        }
    });

    // Handle SVG and images if needed (not here yet)

    // Handle direction-specific classes
    if (translation.dir === 'rtl') {
        document.body.classList.add('rtl');
    } else {
        document.body.classList.remove('rtl');
    }

    // Update active state of buttons
    document.querySelectorAll('button[onclick*="setLanguage"]').forEach(btn => {
        if (btn.getAttribute('onclick').includes(`'${lang}'`)) {
            btn.style.opacity = '1';
            btn.style.transform = 'scale(1.2)';
        } else {
            btn.style.opacity = '0.5';
            btn.style.transform = 'scale(1)';
        }
    });
}

// Initialize
function initTranslations() {
    console.log('Initializing translations...');
    const savedLang = localStorage.getItem('selectedLanguage') || window._pendingLang || 'en';
    window.setLanguage(savedLang);
    delete window._pendingLang;
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initTranslations);
} else {
    initTranslations();
}
